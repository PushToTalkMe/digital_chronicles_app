const prisma = new PrismaClient();

async function main() {
    try {
        const users = await initUsers();
        const facilities = await initFacilities();

        const customerId =
            users.find((user) => user.role === UserRole.CUSTOMER)?.id || '';
        const technicalCustomerId =
            users.find((user) => user.role === UserRole.TECHNICAL_CUSTOMER)
                ?.id || '';

        const result = await updateFacilities(customerId, technicalCustomerId);

        if (!result.success) {
            throw new Error(result.message);
        }

        console.log(result);
    } catch (error) {
        console.log(error);
    }
}

async function initUsers(): Promise<User[]> {
    await prisma.user.deleteMany();

    const users = await prisma.user.createManyAndReturn({
        data: USERS,
    });

    return users;
}

async function initFacilities() {
    const FACILITIES_DTO = FACILITIES.map((FACILITY) => {
        const { coordinates, ...facilityDto } = FACILITY;
        return facilityDto;
    });
    const facilities = await prisma.facility.createManyAndReturn({
        data: FACILITIES_DTO,
    });

    return facilities;
}

async function updateFacilities(
    customerId: string,
    technicalCustomerId: string
) {
    try {
        await Promise.all(
            FACILITIES.map(async (FACILITY) => {
                await initPolygonForFacility(FACILITY.coordinates, FACILITY.id);
                await connectCustomersAndTechnicalCustomersToFacility(
                    FACILITY.id,
                    customerId,
                    technicalCustomerId
                );
            })
        );
        return { success: true, message: 'Объекты успешно изменены' };
    } catch (error) {
        return {
            success: false,
            message: 'Ошибка при обновлении объектов: ' + error,
        };
    }
}

async function initPolygonForFacility(
    coordinates: number[][][][],
    facilityId: string
) {
    return await prisma.polygon.create({
        data: {
            coordinates,
            facilityId,
        },
    });
}

async function connectCustomersAndTechnicalCustomersToFacility(
    facilityId: string,
    customerId: string,
    technicalCustomerId: string
) {
    await prisma.facility.update({
        where: { id: facilityId },
        data: {
            user: {
                connect: [
                    {
                        id: customerId,
                    },
                    {
                        id: technicalCustomerId,
                    },
                ],
            },
        },
    });
}

